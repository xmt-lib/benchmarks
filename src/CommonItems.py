from random import Random
import os

name = os.path.splitext(os.path.basename(__file__))[0]
logic = "LIA"
result = "unsat"

def generate_data(size):
    prng = Random(f"{id}{size}")

    ps = []
    qs = []
    for i in range(0, size):
        if prng.randint(0, 1):
            ps.append(f'{i}')
        else:
            qs.append(f'{i}')
    elems = [f'{i}' for i in range(0, size)]
    return ps, qs, elems

def smt(size=200000):
    ps, qs, elems = generate_data(size)

    p_expr = "\n  ".join([f'(= x {p})' for p in ps])
    q_expr = "\n  ".join([f'(= y {q})' for q in qs])

    smt = f"""(set-info :smt-lib-version 2.6)
(set-logic {logic})
(set-info :source |
Generated by: Pierre Carbonnelle
Generated on: 2026-04-28
Generator: https://github.com/xmt-lib/benchmarks
Application: industrial configuration problem
Target solver: Z3
Publications: https://arxiv.org/abs/2602.19102
Time limit: 300
From the DIRT benchmark used to evaluate grounders
|)
(set-info :license "https://creativecommons.org/licenses/by/4.0/")
(set-info :category "crafted")
(set-info :status {result})

(define-fun p ((x Int)) Bool (or
  {p_expr}
))
(define-fun q ((y Int)) Bool (or
  {q_expr}
))
(assert (exists ((x Int)) (and (p x) (q x))))

(check-sat)
(exit)
"""
    return smt

def asp(size=200000):
    ps, qs, elems = generate_data(size)

    elems_str = "; ".join(elems)
    ps_str = "; ".join(ps)
    qs_str = "; ".join(qs)

    asp_str = f"""node({elems_str}).
p({ps_str}).
q({qs_str}).
ok :- p(X), q(X).
:- not ok.
"""

    return asp_str


def sli(size=200000):
    ps, qs, elems = generate_data(size)
    elems_str = ", ".join(elems)
    ps_str = ", ".join(ps)
    qs_str = ", ".join(qs)

    return f"""
vocabulary V {{
    type Domain := {{{elems_str}}}
    p: Domain -> Bool
    q: Domain -> Bool
}}
theory T: V {{
    ?x in Domain: p(x) & q(x).
}}
structure S: V {{
    p := {{{ps_str}}}.
    q := {{{qs_str}}}.
}}
procedure main() {{
    stdoptions.nbmodels = 1
    printmodels(modelexpand(T, S))
}}
"""

def idp3(size=200000):
    ps, qs, elems = generate_data(size)
    elems_str = "; ".join(elems)
    ps_str = "; ".join(ps)
    qs_str = "; ".join(qs)

    return f"""
vocabulary V {{
    type Domain
    p(Domain)
    q(Domain)
}}
theory T: V {{
    ?x[Domain]: p(x) & q(x).
}}
structure S: V {{
    Domain = {{{elems_str}}}
    p = {{{ps_str}}}
    q = {{{qs_str}}}
}}
procedure main() {{
    stdoptions.nbmodels = 1
    printmodels(modelexpand(T, S))
}}
"""
