    ps = []
    qs = []
    for i in range(0, size):
        if prng.randint(0, 1):
            ps.append(f'a{i}')
        else:
            qs.append(f'a{i}')

    elems_str = ") (".join(elems)
    p_expr = "\n  ".join([f'(= x {p})' for p in ps])
    q_expr = "\n  ".join([f'(= y {q})' for q in qs])

