from random import Random
import os

name = os.path.splitext(os.path.basename(__file__))[0]
logic = "UFDTLIA"
result = "unsat"

def smt(size=2500, density=0.01):
    prng = Random(f"GraphColoring-{size}-{density}")
    graph = [(int((number) / size + 1), number % size + 1)
        for number in prng.sample(range(size * size), int(size * size * density))]

    nodes_str = " ".join([f'(n{i})' for i in range(1, size + 1)])
    edge_expr = "\n  ".join([f"(and (= x n{a}) (= y n{b}))" for (a, b) in graph])

    smt = f"""(set-info :smt-lib-version 2.6)
(set-logic {logic})
(set-info :source |
Generated by: Pierre Carbonnelle
Generated on: 2026-04-28
Generator: https://github.com/xmt-lib/benchmarks
Application: industrial configuration problem
Target solver: Z3
Publications: https://arxiv.org/abs/2602.19102
Time limit: 300
From the DIRT benchmark used to evaluate grounders
|)
(set-info :license "https://creativecommons.org/licenses/by/4.0/")
(set-info :category "random")
(set-info :status {result})

(set-option :smt.auto-config false)
(set-option :smt.ematching false)

(declare-datatype Node ( {nodes_str} ))
(declare-datatype Color ((red) (blue) (green) (orange) (purple)))
(declare-fun edge (Node Node) Bool)
(declare-fun colorOf (Node) Color)
(assert
    (forall ((x Node) (y Node))
        (= (edge x y) (or
  {edge_expr}
        ))
    )
)
(assert
    (forall ((x Node) (y Node))
        (=> (edge x y) (not (= (colorOf x) (colorOf y))))
    )
)
(check-sat)
(exit)
"""
    return smt
