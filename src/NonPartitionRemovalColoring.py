from random import Random
import re
import os

from src.run import run_cvc5, run_xmt, run_z3

name = os.path.splitext(os.path.basename(__file__))[0]


## create the sets

with open("src/NonPartition_1000_2_7.asp") as fd:
    file_str = fd.read()
vertex = list()
for n in re.finditer(r"(?m)^vertex\(([^,\)]+)\)\.", file_str):
    vertex.append(int(n.group(1)))
edge = list()
for li in re.finditer(r"(?m)^edge\(([^,\)]+),([^,\)]+)\)\.", file_str):
    edge.append((int(li.group(1)), int(li.group(2))))

#  Run the scripts

logic = "UFDTLIA"
result = "unsat"

smt = f"""
(set-info :smt-lib-version 2.6)
(set-logic {logic})
(set-info :source |
Generated by: Pierre Carbonnelle
Generated on: 2026-04-28
Generator: https://github.com/xmt-lib/benchmarks
Application: industrial configuration problem
Target solver: Z3
Publications: https://arxiv.org/abs/2602.19102
Time limit: 600
From the DIRT benchmark used to evaluate grounders
|)
(set-info :license "https://creativecommons.org/licenses/by/4.0/")
(set-info :category "random")
(set-info :status {result})

(declare-datatype Node ( {" ".join([f'(a{i})' for i in vertex])} ))
(declare-datatype Color ( (red) (green) (blue) ))

(define-fun edge ((x Node) (y Node)) Bool (or
  {'\n  '.join([f'  (and (= x a{n1}) (= y a{n2}))' for (n1, n2) in edge])}
))
(declare-fun keep (Node) Bool)
(declare-fun kept_edge (Node Node) Bool)
(declare-fun color (Node) Color)

(assert (forall ((x Node) (y Node))
                (or (keep x) (keep y) (= x y))))
(assert (forall ((x Node) (y Node))
                (= (kept_edge x y)
                   (and (keep x) (keep y) (edge x y)))))
(assert (forall ((x Node) (y Node))
                (=> (kept_edge x y)
                    (not (= (color x) (color y))))))
(assert (forall ((x Node))
                (=> (not (keep x))
                    (= (color x) red))))

(check-sat)
"""

run_z3(smt, logic, name, result)
run_cvc5(smt, name, result)

xmt = f"""
    (set-option :backend Z3)

    (declare-datatype Vertex ( {" ".join([f'(a{i})' for i in vertex])} ))
    (declare-datatype Color ( (red) (green) (blue) ))

    (declare-fun edge (Vertex Vertex) Bool)
    (declare-fun keep (Vertex) Bool)
    (declare-fun kept_edge (Vertex Vertex) Bool)
    (declare-fun color (Vertex) Color)

    ;     keep(X) :- vertex(X), not delete(X).
    ;     delete(X) :- vertex(X), not keep(X).
    ; means that keep has domain Vertex


    ; :- delete(X), vertex(Y), not keep(Y), X != Y.

    (assert (forall ((x Vertex) (y Vertex))
                    (or (keep x) (keep y) (= x y))))

    ; kept_edge(V1, V2) :- keep(V1), keep(V2), edge(V1, V2).
    (assert (forall ((x Vertex) (y Vertex))
                    (= (kept_edge x y)
                    (and (keep x) (keep y) (edge x y)))))

    ;     blue(N) :- keep(N), not red(N), not green(N).
    ;     red(N) :- keep(N), not blue(N), not green(N).
    ;     green(N) :- keep(N), not red(N), not blue(N).
    ; means that color is a function

    ; :- kept_edge(N1,N2), blue(N1), blue(N2).
    ; :- kept_edge(N1,N2), red(N1), red(N2).
    ; :- kept_edge(N1,N2), green(N1), green(N2).
    (assert (forall ((x Vertex) (y Vertex))
                    (=> (kept_edge x y)
                        (not (= (color x) (color y))))))

    (x-interpret-pred edge (x-set {' '.join([f'(a{n1} a{n2})' for (n1, n2) in edge])}))

    (check-sat)
"""
run_xmt(xmt, name, result)

