def read_data(file_path):
    with open(file_path) as fd:
        file_str = fd.read()
    t: list[tuple[int, int]] = list()
    for n in re.finditer(r"(?m)^t\(([^,\)]+)\,([^,\)]+)\)\.", file_str):
        t.append((int(n.group(1)), int(n.group(2))))
    p = list()
    for li in re.finditer(r"(?m)^p\(([^,\)]+),([^,\)]+)\)\.", file_str):
        p.append((int(li.group(1)), int(li.group(2))))
    colour = list()
    for col in re.finditer(r"(?m)^colour\(([^,\)]+)\)\.", file_str):
        colour.append(col.group(1))
    patternlength = int(re.findall(r"(?m)^patternlength\(([^,\)]+)\)\.", file_str)[0])
    max_number = max(t, key=lambda x: x[0])[0]
    return file_str, t, p, colour, patternlength, max_number

