from random import Random
import re
import os

from src.run import run_cvc5, run_xmt, run_z3

name = os.path.splitext(os.path.basename(__file__))[0]


## create the sets

with open("src/PackingProblem-45-0-0.asp") as fd:
    file_str = fd.read()
square: list[tuple[int, int]] = list()
for sq in re.finditer(r"(?m)^square\(([^,\)]+),([^,\)]+)\)\.", file_str):
    square.append((int(sq.group(1)), int(sq.group(2))))
max_square_num = int(re.findall(r"(?m)^max_square_num\(([^,\)]+)\)\.", file_str)[0])
area_match = re.findall(r"(?m)^area\(([^,\)]+),([^,\)]+)\)\.", file_str)[0]
area = (int(area_match[0]), int(area_match[1]))

size = "0"
for (sq, s) in reversed(square):
    size = f"(ite (= x q{sq}) {s}\n  {size})"

#  Run the scripts

logic = "UFDTLIA"
result = "sat"

smt = f"""
(set-info :smt-lib-version 2.6)
(set-logic {logic})
(set-info :source |
Generated by: Pierre Carbonnelle
Generated on: 2026-04-28
Generator: https://github.com/xmt-lib/benchmarks
Application: industrial configuration problem
Target solver: Z3
Publications: https://arxiv.org/abs/2602.19102
Time limit: 600
From the DIRT benchmark used to evaluate grounders
|)
(set-info :license "https://creativecommons.org/licenses/by/4.0/")
(set-info :category "random")
(set-info :status {result})

(declare-datatype Square ( {" ".join([f'(q{i})' for i in range(1, max_square_num + 1)])} ))

(declare-fun pos_x (Square) Int)
(declare-fun pos_y (Square) Int)
(declare-fun square_size (Square) Int)

{"\n".join([f'(assert (= (square_size q{square}) {size}))' for (square, size) in square])}

(declare-fun area_width () Int)
(declare-fun area_height () Int)

(assert (forall ((s Square)) (<= 0 (pos_x s))))
(assert (forall ((s Square)) (<= 0 (pos_y s))))
(assert (forall ((s Square)) (<= (+ (pos_x s) (square_size s)) area_width)))
(assert (forall ((s Square)) (<= (+ (pos_y s) (square_size s)) area_height)))

(assert (forall ((s1 Square) (s2 Square))
                (not (and (not (= s1 s2))

                          (<= (pos_x s1) (pos_x s2))
                          (<= (pos_y s1) (pos_y s2))

                          (< (pos_x s2) (+ (pos_x s1) (square_size s1)))
                          (< (pos_x s1) (+ (pos_x s2) (square_size s2)))
                          (< (pos_y s2) (+ (pos_y s1) (square_size s1)))
                          (< (pos_y s1) (+ (pos_y s2) (square_size s2)))

                          (<= (+ (pos_x s2) (square_size s2)) (+ (pos_x s1) (square_size s1)))
                          (<= (+ (pos_y s2) (square_size s2)) (+ (pos_y s1) (square_size s1)))
                     )
              )))
(assert (= area_width  {area[0]}))
(assert (= area_height {area[1]}))

(check-sat)
"""

run_z3(smt, logic, name, result)
run_cvc5(smt, name, result)

xmt = f"""
    (set-option :backend Z3)

    (declare-datatype Square ( {" ".join([f'(q{i})' for i in range(1, max_square_num + 1)])} ))

    (declare-fun pos_x (Square) Int)
    (declare-fun pos_y (Square) Int)
    (declare-fun square_size (Square) Int)
    (declare-fun area_width () Int)
    (declare-fun area_height () Int)

    (assert (forall ((s Square)) (<= 0 (pos_x s))))
    (assert (forall ((s Square)) (<= 0 (pos_y s))))
    (assert (forall ((s Square)) (<= (+ (pos_x s) (square_size s)) area_width)))
    (assert (forall ((s Square)) (<= (+ (pos_y s) (square_size s)) area_height)))

    (assert (forall ((s1 Square) (s2 Square))
                    (not (and (not (= s1 s2))

                            (<= (pos_x s1) (pos_x s2))
                            (<= (pos_y s1) (pos_y s2))

                            (< (pos_x s2) (+ (pos_x s1) (square_size s1)))
                            (< (pos_x s1) (+ (pos_x s2) (square_size s2)))
                            (< (pos_y s2) (+ (pos_y s1) (square_size s1)))
                            (< (pos_y s1) (+ (pos_y s2) (square_size s2)))

                            (<= (+ (pos_x s2) (square_size s2)) (+ (pos_x s1) (square_size s1)))
                            (<= (+ (pos_y s2) (square_size s2)) (+ (pos_y s1) (square_size s1)))
                        )
                )))

    (x-interpret-const area_width {area[0]})
    (x-interpret-const area_height {area[1]})

    (x-interpret-fun square_size (x-mapping {" ".join([f'((q{x[0]}) {x[1]})' for x in square])} ))

    (check-sat)
"""
run_xmt(xmt, name, result)

