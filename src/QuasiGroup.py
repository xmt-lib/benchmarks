def read_data(file_path):
    with open(file_path) as fd:
        file_str = fd.read()

    state: list[tuple[int, int, int]] = list()
    for sq in re.finditer(r"(?m)^state\(([^,\)]+),([^,\)]+),([^,\)]+)\)\.", file_str):
        state.append((int(sq.group(1)), int(sq.group(2)), int(sq.group(3))))

    domain: list[int] = list()
    for sq in re.finditer(r"(?m)^domain\(([^,\)]+)\)\.", file_str):
        domain.append(int(sq.group(1)))

    v: list[tuple[int, int]] = list()
    for sq in re.finditer(r"(?m)^v\(([^,\)]+),([^,\)]+)\)\.", file_str):
        v.append((int(sq.group(1)), int(sq.group(2))))

    return file_str, state, domain, v

