from random import Random
import re
import os

name = os.path.splitext(os.path.basename(__file__))[0]
logic = "UFLIA"
result = "sat"

def smt(file_path="src/QuasiGroup-d_50-p_60.asp"):
    with open(file_path) as fd:
        file_str = fd.read()

    state: list[tuple[int, int, int]] = list()
    for sq in re.finditer(r"(?m)^state\(([^,\)]+),([^,\)]+),([^,\)]+)\)\.", file_str):
        state.append((int(sq.group(1)), int(sq.group(2)), int(sq.group(3))))

    domain: list[int] = list()
    for sq in re.finditer(r"(?m)^domain\(([^,\)]+)\)\.", file_str):
        domain.append(int(sq.group(1)))

    v: list[tuple[int, int]] = list()
    for sq in re.finditer(r"(?m)^v\(([^,\)]+),([^,\)]+)\)\.", file_str):
        v.append((int(sq.group(1)), int(sq.group(2))))

    v_expr = "\n  ".join([f"(and (= x {a}) (= y {b}))" for (a,b) in v])
    state_expr = "\n  ".join([f"(and (= x {a}) (= y {b}) (= z {c}))" for (a,b,c) in state])

    smt = f"""(set-info :smt-lib-version 2.6)
(set-logic {logic})
(set-info :source |
Generated by: Pierre Carbonnelle
Generated on: 2026-04-28
Generator: https://github.com/xmt-lib/benchmarks
Application: industrial configuration problem
Target solver: Z3
Publications: https://arxiv.org/abs/2602.19102
Time limit: 300
From the DIRT benchmark used to evaluate grounders
|)
(set-info :license "https://creativecommons.org/licenses/by/4.0/")
(set-info :category "random")
(set-info :status {result})

; cannot use a datatype because of '<'
(define-fun Domain ((x Int)) Bool (<= 1 x {max(*domain)}))

(define-fun v ((x Int) (y Int)) Bool (or
  {v_expr}
))
(define-fun state ((x Int) (y Int) (z Int)) Bool (or
  {state_expr}
  false  ; if empty
))
(declare-fun assign (Int Int Int) Bool)
(declare-fun assigned (Int) Bool)

(assert (forall ((V Int))
            (=> (Domain V)
                (= (assigned V)
                   (exists ((x Int) (y Int))
                      (and (Domain x) (Domain y)
                           (assign V x y)))))))

(assert (forall ((V Int) (x1 Int) (x2 Int) (y Int))
            (=> (and (Domain V) (Domain x1) (Domain x2) (Domain y))
                (not (and (assign V x1 y) (assign V x2 y) (< x1 x2))))))

(assert (forall ((V Int) (x Int) (y1 Int) (y2 Int))
            (=> (and (Domain V) (Domain x) (Domain y1) (Domain y2))
                (not (and (assign V x y1) (assign V x y2) (< y1 y2))))))

(assert (forall ((s Int) (x Int) (y Int))
            (=> (and (Domain s) (Domain x) (Domain y))
                (not (and (state x y s) (not (assign s x y)))))))

(assert (forall ((V Int))
            (=> (Domain V)
                (not (not (assigned V))))))

(check-sat)
"""
    return smt
