def generate_data(size):
    edge = [(i, j) for x in range(0, size) for i, j in [(x, x+1), (x, x+2)] if j < size]
    p = [(i, i+1, i+2) for i in range(0, size) if i+2 < size]
    return edge, p

def smt(size=250):
    edge, p = generate_data(size)

    nodes_str = " ".join(f'(a{i})' for i in range(0, size))
    edge_expr = "\n  ".join([f"(and (= x a{a}) (= y a{b}))" for (a,b) in edge])
    p_expr = "\n  ".join([f"(and (= x a{a}) (= y a{b}) (= z a{c}))" for (a,b,c) in p])

    smt = f"""(set-info :smt-lib-version 2.6)
(set-logic {logic})
(set-info :source |
Generated by: Pierre Carbonnelle
Generated on: 2026-04-28
Generator: https://github.com/xmt-lib/benchmarks
Application: industrial configuration problem
Target solver: Z3
Publications: https://arxiv.org/abs/2602.19102
Time limit: 300
From the DIRT benchmark used to evaluate grounders
|)
(set-info :license "https://creativecommons.org/licenses/by/4.0/")
(set-info :category "crafted")
(set-info :status {result})

(declare-datatype Node ( {nodes_str} ))
(define-fun edge ((x Node) (y Node)) Bool (or
  {edge_expr}
))
(define-fun p ((x Node) (y Node) (z Node)) Bool (or
  {p_expr}
))
(assert (forall ((x Node) (y Node) (z Node))
                (= (p x y z)
                   (and (edge x y) (edge y z) (edge x z)) )))

(check-sat)
(exit)
"""
    return smt

def asp(size=250):
    edge, p = generate_data(size)

    nodes_str = "\n".join(f'node(a{i}).' for i in range(0, size))
    edge_str = "\n".join([f"edge(a{a},a{b})." for (a,b) in edge])
    p_str = "\n".join([f"p(a{a},a{b},a{c})." for (a,b,c) in p])

    asp_str = f"""{nodes_str}
{edge_str}
{p_str}
triangle(X,Y,Z) :- edge(X,Y),edge(Y,Z),edge(X,Z).
:- triangle(X,Y,Z), not p(X,Y,Z).
:- not triangle(X,Y,Z),p(X,Y,Z).
"""
    return asp_str
